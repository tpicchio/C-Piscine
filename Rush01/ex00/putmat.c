#include <unistd.h>
#include <stdio.h>

void	ft_putmat(int mat[4][4])
{
	int	i;
	int	j;
	char	tmp;
	
	i = 0;
	j = 0;
	while(i < 4)
	{
		j = 0;
		while(j < 4)
		{
			tmp = mat[i][j] + 48;
			write (1, &tmp, 1);
			write (1, " ", 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}
}
